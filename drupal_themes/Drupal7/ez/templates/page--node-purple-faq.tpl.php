          <li>What is a text message (SMS)?</li>
          <p>SMS stands for Short Message Service, and it is synonymous with text message, text, TXT, etc. An SMS (text message) is a short message, which can be sent to and from a cellular phone.</p>
          <li>Can my phone receive text messages?</li>
          <p>Nearly all phones purchased in the last ten years are capable of sending and receiving text messages.</p>
          <li>Does my carrier (or my contact's carrier) charge for received messages?</li>
          <p>Every carrier offers different text messaging packages.</p>
          <li>How many characters may I include in a text message?</li>
          <p>SMS messages are limited to 160 characters. This includes numbers, spaces, symbols, and punctuation.</p>
          <li>How long does it take to deliver a text message?</li>
          <p>Text messages are usually delivered to mobile phones within a few seconds to a few minutes. During peak hours, there could be a short delay. Please note that additional delays may occur if the recipient's phone is turned off or is outside of the coverage area. When this happens, a carrier will attempt to deliver your message for up to 72 hours.</p>
          <li>What is a short code?</li>
          <p>A short code is a five- or six-digit number to which text messages can be sent from a mobile phone. Wireless subscribers send text messages to short codes to access a wide variety of mobile content. Common short codes are easy to remember and are compatible across all participating carriers.</p>
          <li>Can you guarantee delivery of every text message that I send?</li>
          <p>No. As text messaging is a store-and-forward technology that is dependent on carriers and their networks, it is impossible to guarantee delivery of a text message.</p>
          <li>Can I change the number or set a name that you send my text messages from?</li>
          <p>Unfortunately, it isn't possible to send a message from a name as phone calls and text messages always come from phone numbers. There are two different kinds of phone numbers:  regular ten-digit numbers like the one you have on your cell phone, and short coded, which are special five- or six-digit numbers like ours. For a message to appear from a name it must be stored in your phone’s address book. You will notice when you receive a call or text message from a person who is not in your phone book, it appears as their ten-digit number (not a name), since it is not stored in your phone. The same applies to a short code.</p>
